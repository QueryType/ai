all_names = []

# Add all direct categories
all_names.extend(historical)
all_names.extend(mythological)
all_names.extend(sanskrit_names)
all_names.extend(places)
all_names.extend(sanskrit_words)

print(f"Direct names: {len(all_names)}")

# Generate combinations: prefix + suffix style names
# This is the main augmentation to reach ~32K
generated = set()
for _ in range(40000):
    strategy = random.choice([1, 2, 3, 4, 5])

    if strategy == 1:
        # prefix + suffix
        name = random.choice(prefixes) + random.choice(suffixes)
    elif strategy == 2:
        # prefix + name_fragment
        base = random.choice(all_names)
        # Take last 2-5 chars
        chars = list(base)
        n = min(len(chars), random.randint(2, 5))
        fragment = ''.join(chars[-n:])
        name = random.choice(prefixes) + fragment
    elif strategy == 3:
        # name_fragment + suffix
        base = random.choice(all_names)
        chars = list(base)
        n = min(len(chars), random.randint(2, 5))
        fragment = ''.join(chars[:n])
        name = fragment + random.choice(suffixes)
    elif strategy == 4:
        # Two name fragments merged
        base1 = random.choice(all_names)
        base2 = random.choice(all_names)
        chars1 = list(base1)
        chars2 = list(base2)
        n1 = min(len(chars1), random.randint(2, 4))
        n2 = min(len(chars2), random.randint(2, 4))
        name = ''.join(chars1[:n1]) + ''.join(chars2[-n2:])
    elif strategy == 5:
        # Just use an existing name (oversampling)
        name = random.choice(all_names)

    if len(name) >= 2 and len(name) <= 20:
        generated.add(name)

all_names_set = set(all_names)
all_names_set.update(generated)
all_names = list(all_names_set)

# Oversample shorter, more natural-sounding names to balance dataset
# Names from categories 1-4 get repeated more
core_names = historical + mythological + sanskrit_names + places
for _ in range(5):
    all_names.extend(core_names)

random.shuffle(all_names)

# Trim or pad to target
target = 32000
if len(all_names) > target:
    all_names = all_names[:target]
elif len(all_names) < target:
    # oversample to fill
    while len(all_names) < target:
        all_names.append(random.choice(core_names))

random.shuffle(all_names)

print(f"Final dataset size: {len(all_names)}")

# Count unique chars
